# Pengenalan Operasi Matematika dalam Python

# Penjumlahan
a = 10
b = 5
hasiljumlah = a + b
print("Penjumlahan:", hasiljumlah)

# Pengurangan
hasilq = a - b
print("Pengurangan:",hasilq)

# Perkalian
hasilx = a * b
print("Perkalian:", hasilx)

# Pembagian
hasilb = a / b
print("Pembagian:", hasilb)

# Pembagian Bulat
hasilbb = a // b
print("Pembagian Bulat:", hasilbb)

# Modulus (Sisa Pembagian)
hasilm = a % b
print("Modulus:", hasilm)

# Pangkat
hasilp = a ** b
print("Pangkat:", hasilp)

# Pengenalan Variabel dalam Python

# Mendefinisikan variabel
nama = "Shafwan"  # String
umur = 20       # Integer
tinggi = 170.0 # Float
dia_mahasiswa = True # Boolean

# Menampilkan nilai variabel
print("Nama:", nama)
print("Usia:", umur)
print("Tinggi:", tinggi)
print("Apakah seorang mahasiswa?", dia_mahasiswa)

# Mengubah nilai variabel
nama = "aulia"
umur = 20
dia_mahasiswa = False

# Menampilkan nilai variabel yang telah diubah
print("\nSetelah perubahan:")
print("Nama:", nama)
print("Usia:", umur)
print("Apakah seorang mahasiswa?", dia_mahasiswa)
